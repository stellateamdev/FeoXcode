import Foundation
import UIKit

extension UIColor {
    class func stellaPurple() -> UIColor {
        return UIColor.init(red: 151/255.0, green: 49/255.0, blue: 156/255.0, alpha: 1)
    }
    class func mapBackground() -> UIColor {
        return UIColor(red: 247/255, green: 243/255, blue: 240/255, alpha: 1)
    }
    class func RGB(r:CGFloat,g:CGFloat,b:CGFloat) -> UIColor {
        return UIColor(red: r/255.0, green: g/255.0, blue: b/255.0, alpha: 1.0)
    }
}
